public class Variavel {
    public static void main(String[] args) {
        int salario1 = 500;
        int salario2 = 600;

        if (salario1>salario2) {
            System.out.println("É maior");
        }else if (salario1 == salario2) {
            System.out.println("É igual");
        }else {
            System.out.println("É menor");
        }
    }
}